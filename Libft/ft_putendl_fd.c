#include "libft.h"

void	ft_putendl_fd(char *s, int fd)

{
	int	cpt;

	if (s == NULL)
		return ;
	cpt = 0;
	while (s[cpt])
	{
		cpt++;
	}
	write(fd, s, cpt);
	write(fd, "\n", 1);
}
