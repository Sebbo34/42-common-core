#include "libft.h"

void	ft_bzero(void *s, size_t n)

{
	size_t			cpt;
	unsigned char	*ptr;

	ptr = (unsigned char *) s;
	cpt = 0;
	while (cpt < n)
	{
		ptr[cpt] = '\0';
		cpt++;
	}
}
