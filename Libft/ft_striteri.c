#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))

{
	int	cpt;

	if (s == NULL)
		return ;
	cpt = 0;
	while (s[cpt])
	{
		(*f)(cpt, &s[cpt]);
		cpt++;
	}
}
